Habboshoot est un jeu d'assassinat qui se d&eacute;roule dans Habbo
H&ocirc;tel.<br>
Les killers cherchent des victimes
dans une liste, l'espionnent, et la tuent.
<p>Comment tuer un habbo ?
</p>
<ol>
  <li> Inscris-toi sur Habboshoot.</li>
  <li>Dans la liste des killers, choisis une victime.</li>
  <li>Dans Habbo h&ocirc;tel, colle-toi juste derri&egrave;re elle et crie le mot kil. (Ce mot est
pr&eacute;cis&eacute; chaque partie.)&nbsp;</li>
  <li>Prend imm&eacute;diatement un screen, si tu ne sais pas
en faire,
un tutorial est disponible <a href="index.php?pg=news&id=5">ici</a>.</li>
  <li>Reviens sur le site habboshoot et fait un rapport
d'assassinat.&nbsp;</li>
  <li>Tu recevras des pi&egrave;ces, tu pourras acheter armes
et bonus.</li>
</ol>
