<p>Sur Habboshoot, la semaine est divisée en deux. La première partie dure 3 jours : mardi, mercredi, jeudi. La seconde dure 2 jours : samedi et dimanche. Si tu meurs pendant une partie, tu ne peux plus jouer (sauf si t'as assez de pièces pour ressusciter). Entre deux parties, c'est-à-dire le lundi et le vendredi, tout le monde est ressuscité. 
</p>
<p>R&egrave;gles particuli&egrave;res :
</p>
<ul>
  <li> Un habbo avec un sac sur la t&ecirc;te où avec le FX fantôme ne peut
&ecirc;tre
tu&eacute; et ne peux tuer ; Il est anonyme. </li>
  <li> Les kills
sont interdits dans les salles o&ugrave; il y a un rayon x HC
devant la porte. </li>
  <li> Les kills
sont autoris&eacute;s dans tous les endroits de l'h&ocirc;tel.
(Piscines, salles de snow...)
.</li>
  <li> On ne peut pas tuer si la victime est plac&eacute;e
dans une case o&ugrave; il peut y avoir plusieurs habbo. </li>
  <li>Il est interdit de tuer en marchant, en étant allongé ou en chevauchant un animal.</li>
  <li>La bulle qui contient le mot kill doit être criée en majuscule (sauf pour la partie .hbsh.fr), à la verticale du killeur.
  </li>
  <li> La victime et le killeur doivent &ecirc;tre
visibles.
  </li>
  <li>Le killeur doit avoir cliqué sur la victime avec flèche au-dessus (Pour avoir son pseudo à droite et la flèche pour son emplacement) .</li>
  <li> Une balle est retir&eacute;e &agrave; chaque
rapport d'assassinat envoy&eacute;. (Ou un couteau si c'est un
assassinat avec celui-ci).
  </li>
  <li> Deux comptes maximum par connexion internet. (= clones) </li>
  <li> Les tricheurs seront bannis d&eacute;finitivement.</li>
  <li> Il est interdit de tuer son clone. </li>
  <li>On ne peut pas tuer un habbo s'il y a au moins un autre habbo sur sa case.</li>
  <li>Un habbo qui ne s'est pas connecté à habboshoot pendant 14 jours est considéré comme inactif. Il ne peut plus être tué.</li>
</ul>
La KeyTime est un petit code de 9 caractères qui change toutes les heures. C'est-à-dire que la keytime de 9h00 est différente de celle de 10h00. Sa présence est obligatoire sur chaque screen.
Lorsque tu kill un habbo, tu as toujours au moins une heure pour faire ton rapport d'assassinat. Comme les deux dernières keytimes sont valables, le délai est entre 1heure et 2heures. ( Par exemple, si tu tues un habbo entre 13h et 13h59, tu auras jusqu'à 14h59 pour poster )
<br/>
<br/>
Tu peux voter pour valider des kills. Tu peux valider les kills où tu es la victime. Tu peux refuser les kills où tu es le killer. Cependant, tu dois avoir tué au moins 5 personnes.
<br/>

Chaque killeur doit respecter l'habbo attitude. Habboshoot est open source, n'importe qui peut avoir une copie du code source du site.
